package Jeu;

public class Conneries {

	private int nbCoup;
	private int score;	
	
	public Conneries() {
		this.nbCoup = 0;
		this.score = 0;
	}

	public int getNbCoup() {
		return nbCoup;
	}
	
	public int getScore() {
		return score;
	}
	
	public void setNbCoup(int nbCoup) {
		this.nbCoup = nbCoup;
	}
	
	public void setScore(int score) {
		this.score = score;
	}
	
	
	
	
}